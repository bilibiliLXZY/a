BOOL EnableDebugPrivilege(BOOL fEnable)
{
    BOOL fOk = FALSE;
    HANDLE hToken;
    if (OpenProcessToken(GetCurrentProcess(), TOKEN_ADJUST_PRIVILEGES, &hToken))
    {
        TOKEN_PRIVILEGES tp;
        tp.PrivilegeCount = 1;
        LookupPrivilegeValue(NULL, SE_DEBUG_NAME, &tp.Privileges[0].Luid);
        tp.Privileges[0].Attributes = fEnable ? SE_PRIVILEGE_ENABLED : 0;
        AdjustTokenPrivileges(hToken, FALSE, &tp, sizeof(tp), NULL, NULL);
        fOk = (GetLastError() == ERROR_SUCCESS);
        CloseHandle(hToken);
    }
    return(fOk);
}

BOOL CallNtSetInformationProcess(HANDLE hProcess, ULONG Flag)
{
    _NtSetInformationProcess NtSetInformationProcess = (_NtSetInformationProcess)GetProcAddress(GetModuleHandleA("NtDll.dll"), "NtSetInformationProcess"); //NTDLL.dll:set information
    if (!NtSetInformationProcess)
    {
        return 0; //exit
    }
    if(NtSetInformationProcess(hProcess, (PROCESS_INFORMATION_CLASS)ProcessBreakOnTermination, &Flag, sizeof(ULONG))<0)
        return 0; //exit
    return 1; //exit...?
}

DWORD GetProcessID(char *FileName)
{
    HANDLE myhProcess;
    PROCESSENTRY32 mype;
    mype.dwSize = sizeof(PROCESSENTRY32); 
    BOOL mybRet;
    myhProcess = CreateToolhelp32Snapshot(TH32CS_SNAPPROCESS,0);
    mybRet = Process32First(myhProcess,&mype);
    while(mybRet)
    {
    if(strcmp(FileName,mype.szExeFile)==0) return mype.th32ProcessID;
        else mybRet = Process32Next(myhProcess,&mype);
    }
    return 0; //exit
}
